class Solution(object):
    def sum(self, num1, num2):
        """
        :type num1: int
        :type num2: int
        :rtype: int
        """
        return num1 + num2

# Example usage:
solution = Solution()
print(solution.sum(12, 5))    # Output: 17
print(solution.sum(-10, 4))   # Output: -6
